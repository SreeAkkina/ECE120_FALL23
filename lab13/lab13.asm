	ST  R5,COUNTER	    ; value in R5 is stored in COUNTER
	ADD R2,R2,#-1       ; R2 is decremented
	BRz FINISH          ; program sets PC to FINISH when R2 is 0
	BRnp NEXT_0         ; program sets PC to NEXT_0 to loop again when R2 is not 0
FINISH                      ; program moves to FINISH after all the rows are printed
