; This program prints a word in large (8x16) font for each character 
; to the console. The program starts by storing the first address of
; the word in a register and computing the starting address of that
; letter in FONT_DATA. The program then continues to the next letter
; to compute the starting address of that letter. The program does 
; this until it reaches the null part of the string while storing the
; computed starting address in FONT_DATA at the memory location of 
; each of the letters in the string. After computing the starting
; addresses the program moves to the print stage where it loops through
; each of the letters for each row printing 8 characters for each letter
; based on the 0 and 1 for that letter stored in FONT_DATA. After the 
; each of the letters 8 bits are printed in that row the program moves
; to the next row to continue this process until the whole string is 
; printed. When the program reaches the last row the program HALTS.

; R0 - register stores the ascii values to be printed and intermediate
;      values stored in PLACEHOLDER and COUNTER.
; R1 - register is the address register for addresses for each letter
; R2 - register is the counter register for number of rows (16)
; R3 - register is counter for number of characters in word
; R4 - register stores computed start addresses for letters in FONT_DATA
; R5 - register stores multiple addresses used in program
; R6 - register stores what is at the specfic address in FONT_DATA

.ORIG x3000
	AND R2,R2,#0        ; counter register(R2) initialized to 0
	ADD R2,R2,#4        ; counter register(R2) set to 4
	LEA R5,ASCII_CHAR   ; R5 set address of ASCII_CHAR
	LDR R5,R5,#0        ; R5 set to whats contained in ASCII_CHAR
	LDR R4,R5,#0        ; R4 set to the whats contained at address R5
	AND R3,R3,#0        ; R3 (letter counter) initialized to 0
FIND_ADDRESS		    ; program loops to FIND_ADDRESS while R4 is not NULL
	ADD R4,R4,R4        ; R4 is added to R4 (4 times)
	ADD R2,R2,#-1       ; counter R2 is decremented
	BRz NEXT            ; program sets PC to NEXT when R2 is 0
	BRp FIND_ADDRESS    ; program sets PC to FIND_ADDRESS when R2 is not 0
NEXT                        ; program moves to NEXT when R2 is 0
	LEA R2,FONT_DATA    ; R2 set to first address of FONT_DATA
	ADD R4,R2,R4	    ; R4 set to starting address of letter in FONT_DATA
	STR R4,R5,#0	    ; address R5 set to contain R4
	ADD R5,R5,#1	    ; R5 incremented
	AND R2,R2,#0	 
	ADD R2,R2,#4	    ; R2 set to 4
	ADD R3,R3,#1	    ; R3 incremented
	ST  R3,PLACEHOLDER  ; PLACEHOLDER contains value in R3 (# letters)
	LDR R4,R5,#0	    ; R4 set to what address R5 contains
	BRz START	    ; program sets PC to START when R4 is 0
	BRp FIND_ADDRESS    ; program sets PC to FIND_ADDRESS to loop again
START			    ; program transitions to the printing stage
	ADD R2,R2,#12       ; R2 set 16 (4+12) as down counter for rows
NEXT_0			    ; programs loops to NEXT_0 at start of each row
	LEA R1,ASCII_CHAR   ; R1 is set to address of ASCII_CHAR
	LDR R1,R1,#0	    ; R1 is set to value at address R1
	LD  R3,PLACEHOLDER  ; R3 set to value in PLACEHOLDER (# letters) 
CHECK			    ; program loops to CHECK while R3 is not 0
	LDR R5,R1,#0        ; R5 is set to value in memory R1
	LD  R0,COUNTER      ; R0 is set to value in COUNTER
	ADD R0,R0,R5	    ; R0 is set address in FONT_DATA
	LDR R6,R0,#0        ; R6 set to value at address R5 in FONT_DATA
	AND R4,R4,#0
	ADD R4,R4,#8	    ; R4 set to 8
	LEA R5,BIT          ; R5 is set to address of BIT
CHECK_1                     ; program loops to CHECK_1 while R4 is not 0
	LDR R0,R5,#0        ; R0 is set to value in specific address in BIT
	AND R0,R6,R0        ; R0 AND R6 to check whether the bit is 0 or 1
	BRz PRINT_ZERO      ; if bit is 0 then PC is set to PRINT_ZERO
	BRnp PRINT_ONE	    ; if bit is 1 then PC is set to PRINT_ONE
PRINT_ZERO                  ; program moves to PRINT_ZERO if bit is 0
	LDI R0,ASCII_ZERO   ; loads value of stored in x5000 to R0
	OUT                 ; prints character in R0 to console
	BRnzp NEXT_1        ; PC set to NEXT_1 to skip PRINT_ONE
PRINT_ONE		    ; program moves to PRINT_ONE if bit is 1
	LDI R0,ASCII_ONE    ; loads value of stored in x5001 to R0
	OUT                 ; prints character in R0 to console
NEXT_1                      ; program moves to NEXT_1 to check R4
	ADD R5,R5,#1        ; R5 is incremented 
	ADD R4,R4,#-1       ; R4 is decremented
	BRz NEXT_2          ; program sets PC to NEXT_2 if R4 is zero
	BRnzp CHECK_1       ; program sets PC to CHECK_1 to loop through again
NEXT_2			    ; program moves to NEXT_2 after each letter segment
	ADD R1,R1,#1	    ; R1 incremented
	ADD R3,R3,#-1	    ; R3 decremented
	BRp CHECK	    ; program sets PC to CHECK if R3 is not zero
NEXT_3                      ; program moves to NEXT_3 after each row is complete
